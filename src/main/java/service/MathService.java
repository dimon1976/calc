package service;

public class MathService {

    public double sum(double a, double b) {
        return a + b;
    }

    public double division(double a, double b) {
        return a / b;
    }

    public double multi(double a, double b) {
        return a * b;
    }

    public double sub(double a, double b) {
        return a - b;
    }
}
